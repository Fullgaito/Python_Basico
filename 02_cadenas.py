"""
Metodos istitle(), title()

Metodo istitle():

Verifica si la primer letra de un string es mayuscula o no si lo es devuelve True sino False.

Metodo title():

Se encarga de convertir la primer letra de un string en mayuscula y las demas en minusculas.

"""

#Ejemplo istitle()

cadena="alejandro"
print(cadena.istitle())

#Ejemplo title()

nombre="serGio alejandro"
print(nombre.title())

print()
"""

Metodos islower(), lower(), isupper(), upper()

Metodo islower():

Verifica si todas las letras de una cadena estan en minusculas devuelve un booleano.

Metodo lower():

Convierte la cadena en minusculas.

Metodo isupper():

Verifica si todas las letras de la cadena estan en mayusculas.

Metodo upper():

Convierte toda la cadena en mayusculas.

"""
print("Ejemplos de islower(), lower(), isupper(), upper()")
print()

#Ejemplo islower()

cadena="HOLA"
print(cadena.islower())

#Ejemplo lower()

print(cadena.lower())

#Ejemplo isupper()

texto="FACEBOOK"
print(texto.isupper())

#Ejemplo upper()
cad="Instagram"
print(cad.upper())