"""
Metodos swapcase(), capitalize(), count()

Metodo swapcase():

Permite convertir una cadena que esta en mayusculas a minusculas y una que esta en minusculas a
mayusculas

Metodo capitalize():

Se utiliza para convertir el primer caracter de un String en una letra mayuscula y las demas en
minusculas

Metodo count():

Determina la cantidad de veces que se encuentra una letra de un String
permite buscar una sub-cadena dentro de la cadena original

"""

#Ejemplo swapcase():

nombre="YOUTUBE"
print(nombre.swapcase())

cad1="Fundamentos De Programacion"
print(cad1.swapcase())

#Ejemplo capitalize():

pais="colomBia"
print(pais.capitalize())

#Ejemplo count()

cad="Venezuela"
print(cad.count("e"))